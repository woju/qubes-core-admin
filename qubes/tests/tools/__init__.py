import sys
try:
    from io import StringIO
except ImportError:
    from StringIO import StringIO


class StdoutBuffer(object):
    def __init__(self):
        self.stdout = StringIO()

    def __enter__(self):
        sys.stdout = self.stdout
        return self.stdout

    def __exit__(self, exc_type, exc_val, exc_tb):
        sys.stdout = sys.__stdout__
        return False
